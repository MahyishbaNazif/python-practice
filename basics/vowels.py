#Find vowels in given string
input_val= input().lower()
vowels= ""
if "a" in input_val:
    vowels += "a"
if "e" in input_val:
    vowels += "e"
if "i" in input_val:
    vowels += "i"
if "o" in input_val:
    vowels += "o"
if "u" in input_val:
    vowels += "u"
print(vowels)
#Time Complexity O(n)    

            
